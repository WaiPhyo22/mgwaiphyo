// ── i18n engine ───────────────────────────────────────────────────────────────
window.I18N = (function () {
  var _lang = 'en';
  try { _lang = localStorage.getItem('lang') || 'en'; } catch (e) {}
  if (!window.TRANSLATIONS[_lang]) _lang = 'en';

  function t(key) {
    var d = window.TRANSLATIONS[_lang] || window.TRANSLATIONS.en;
    return d[key] !== undefined ? d[key] : (window.TRANSLATIONS.en[key] || key);
  }

  function apply(lang) {
    if (!window.TRANSLATIONS[lang]) lang = 'en';
    _lang = lang;
    try { localStorage.setItem('lang', lang); } catch (e) {}

    var d = window.TRANSLATIONS[lang];
    var langAttr = { en: 'en', my: 'my', ja: 'ja' };
    document.documentElement.lang = langAttr[lang] || 'en';

    document.body.classList.remove('lang-en', 'lang-my', 'lang-ja');
    document.body.classList.add('lang-' + lang);

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      var k = el.getAttribute('data-i18n');
      if (d[k] !== undefined) el.textContent = d[k];
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      var k = el.getAttribute('data-i18n-html');
      if (d[k] !== undefined) el.innerHTML = d[k];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      var k = el.getAttribute('data-i18n-placeholder');
      if (d[k] !== undefined) el.placeholder = d[k];
    });

    document.querySelectorAll('.lang-option').forEach(function (btn) {
      btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });

    if (typeof window.updateTypingRoles === 'function' && d.typing_roles) {
      window.updateTypingRoles(d.typing_roles);
    }
  }

  // Wire up segmented toggle buttons
  document.querySelectorAll('.lang-option').forEach(function (btn) {
    btn.addEventListener('click', function () {
      apply(btn.getAttribute('data-lang'));
    });
  });

  // Apply saved language immediately (DOM is ready — scripts are at end of body)
  apply(_lang);

  return { apply: apply, t: t, getCurrentLang: function () { return _lang; } };
}());
